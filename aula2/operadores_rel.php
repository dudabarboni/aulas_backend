<h2> Operadores Relacionais</h2>
<hr>

<p>
    Operadores Relacionais são utilizados
    para comprar e relação entre "coisas"
</p>

<ul>
    <li>>  | Maior</li>
    <li><  | Menor</li>
    <li>>= | Maior Igual</li>
    <li><= | Menor Igual</li>
    <li>!= | Diferente</li>
    <li>== | Igual</li>
</ul>


<p>
    <pre>
    Os operadores relacionais retornam verdadeiro
    (true), ou falso (false). Em PHP true pode ser
    representador por 1 ou uma variavel com valor
    e falso pode ser representado por 0 ou
    inexistencia de valor em uma variável.
</pre>
</p>

<?php

    $valor =10 > 10;
    
    var_dump($valor);

?>